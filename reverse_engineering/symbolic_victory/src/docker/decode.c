void unhex(char* hex) {
    int i, m = 16;
    char v = 0;
    for(i = 0; hex[i] != 0; i++) {
        char c = hex[i];
        if (c >= '0' && c <= '9') {
            v += m*(c-'0');
        } else if (c >= 'a' && c <= 'f') {
            v += m*(10+(c-'a'));
        } else if (c >= 'A' && c <= 'F') {
            v += m*(10+(c-'A'));
        }
        if (m == 16) {
            m = 1;
        } else {
            m = 16; 
            hex[i/2] = v;
            v = 0;
        };
    }
    hex[i/2] = 0;
}

